public class UtilityConversion {
    
    public String getUtilityCode() {
        return utilityCode;
    }

    public void setUtilityCode(String UtilityCode) {
        this.utilityCode = UtilityCode;
    }

    public String getUtilityName() {
        return utilityName;
    }

    public void setUtilityName(String UtilityName) {
        this.utilityName = UtilityName;
    }

    public int getUtilityPrice() {
        return utilityPrice;
    }

    public void setUtilityPrice(int UtilityPrice) {
        this.utilityPrice = UtilityPrice;
    }

    public String getUtilityDamage() {
        return utilityDamage;
    }

    public void setUtilityDamage(String UtilityDamage) {
        this.utilityDamage = UtilityDamage;
    }

    public String getUtilityRange() {
        return utilityRange;
    }

    public void setUtilityRange(String UtilityRange) {
        this.utilityRange = UtilityRange;
    }

    public String getUtilityTime() {
        return utilityTime;
    }

    public void setUtilityTime(String utilityTime) {
        this.utilityTime = utilityTime;
    }

    public String getUtilityAvailability() {
        return UtilityAvailability;
    }

    public void setUtilityAvailability(String UtilityAvailability) {
        this.UtilityAvailability = UtilityAvailability;
    }


    String utilityCode;
    String utilityName;
    int utilityPrice;
    String utilityDamage;
    String utilityRange;
    String utilityTime;
    String UtilityAvailability;

}